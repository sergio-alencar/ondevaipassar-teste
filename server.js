app.get("/api/jogos/:time", (req, res) => {
  const time = req.params.time;
  const jogos = jogosPorTime[time] || [];
  res.json(jogos);
});

// adiciona novo jogo
app.post("/api/jogos/:time", (req, res) => {
  const time = req.params.time;
  const novoJogo = req.body;

  if (!jogosPorTime[time]) {
    jogosPorTime[time] = [];
  }

  jogosPorTime[time].push(novoJogo);
  res
    .status(201)
    .json({ message: "Jogo adicionado com sucesso!", jogo: novoJogo });
});

// edita jogo existente
// Editar um jogo existente
app.put("/api/jogos/:time/:id", (req, res) => {
  const time = req.params.time;
  const id = parseInt(req.params.id);
  const novosDados = req.body;

  if (!jogosPorTime[time] || !jogosPorTime[time][id]) {
    return res.status(404).json({ message: "Jogo não encontrado." });
  }

  jogosPorTime[time][id] = { ...jogosPorTime[time][id], ...novosDados };
  res.json({
    message: "Jogo atualizado com sucesso!",
    jogo: jogosPorTime[time][id],
  });
});

// exclui jogo
app.delete("/api/jogos/:time/:id", (req, res) => {
  const time = req.params.time;
  const id = parseInt(req.params.id);

  if (!jogosPorTime[time] || !jogosPorTime[time][id]) {
    return res.status(404).json({ message: "Jogo não encontrado." });
  }

  const jogoRemovido = jogosPorTime[time].splice(id, 1);
  res.json({ message: "Jogo removido com sucesso!", jogo: jogoRemovido });
});

